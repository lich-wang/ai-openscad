export type MessageKey = keyof typeof messages.en;

export function resolveLocale(locales: readonly string[] = []): Locale {
  return locales.some((locale) => locale.toLowerCase().startsWith("zh"))
    ? "zh"
    : "en";
}

export function getBrowserLocale(): Locale {
  if (typeof navigator === "undefined") {
    return "en";
  }
  return resolveLocale(navigator.languages?.length ? navigator.languages : [navigator.language]);
}

export function t(locale: Locale, key: MessageKey): string {
  return messages[locale][key] ?? messages.en[key];
}
